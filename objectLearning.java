public class Player
{
    private String name;
    private int score = 0;
    public void setName(String n)
    {
        name = n;
    }
    public void setScore(int s)
    {
        score = s;
    }
    public void getName()
    {
        return name;
    }
    
    public void getScore()
    {
        return score;
    }
}
      